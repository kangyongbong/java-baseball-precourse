package baseball.model;

public class Result {

    private int cntStrike;
    private int cntBall;
    private int cntNothing;

    public int getCntStrike() {
        return cntStrike;
    }

    public void setCntStrike(int cntStrike) {
        this.cntStrike = cntStrike;
    }

    public int getCntBall() {
        return cntBall;
    }

    public void setCntBall(int cntBall) {
        this.cntBall = cntBall;
    }

    public int getCntNothing() {
        return cntNothing;
    }

    public void setCntNothing(int cntNothing) {
        this.cntNothing = cntNothing;
    }
}
